# 按日文长度降序排序（精确匹配用，先匹配长串）
TRANS_LIST = sorted(TRANS.items(), key=lambda x: -len(x[0]))

def parse_srt(filepath):
    with open(filepath, 'r') as f:
        content = f.read()
    segments = []
    raw_segments = re.split(r'\n\n+', content.strip())
    for raw in raw_segments:
        lines = raw.strip().split('\n')
        if len(lines) < 2:
            continue
        seq_match = re.match(r'^(\d+)$', lines[0].strip())
        if not seq_match:
            continue
        seq = int(seq_match.group(1))
        tc_match = re.match(r'^(\d{2}:\d{2}:\d{2}[.,]\d{3})\s*-->\s*(\d{2}:\d{2}:\d{2}[.,]\d{3})$', lines[1].strip())
        if not tc_match:
            continue
        start_time = tc_match.group(1)
        end_time = tc_match.group(2)
        text = '\n'.join(lines[2:])
        segments.append({'seq': seq, 'start': start_time, 'end': end_time, 'text': text})
    return segments

def write_srt(segments, filepath):
    with open(filepath, 'w', encoding='utf-8') as f:
        for i, seg in enumerate(segments, 1):
            f.write(f"{i}\n")
            f.write(f"{seg['start']} --> {seg['end']}\n")
            if seg['text']:
                f.write(f"{seg['text']}\n")
            f.write("\n")

def main():
    input_file = "output/CAWD-329-u.srt"
    output_file = "output/CAWD-329-u_中文.srt"
    
    print(f"读取: {input_file}")
    segments = parse_srt(input_file)
    print(f"总段数: {len(segments)}")
    
    # 删除赌博广告（第1-68段是广告）
    clean = segments[68:]  # 从干杯开始
    print(f"删除68段广告，剩余: {len(clean)}")
    
    # 逐段精确匹配翻译
    translated = 0
    kept_original = 0
    still_jp = 0
    
    for seg in clean:
        key = seg['text'].replace('\n', '').strip()
        if not key:
            continue
        
        # 精确匹配
        cn = TRANS.get(key)
        if cn:
            seg['text'] = cn
            translated += 1
        else:
            # 检查是否有日文
            if re.search(r'[\u3040-\u309f\u30a0-\u30ff]', key):
                still_jp += 1
            else:
                kept_original += 1
    
    print(f"翻译: {translated} 段")
    print(f"保留原文(无日文): {kept_original} 段")
    print(f"未翻译(有日文): {still_jp} 段")
    
    # 显示未翻译的日文片段
    if still_jp > 0:
        print(f"\n未翻译的日文片段:")
        for seg in clean:
            key = seg['text'].replace('\n', '').strip()
            if key and key not in TRANS and re.search(r'[\u3040-\u309f\u30a0-\u30ff]', key):
                print(f"  [{key[:60]}]")
    
    # 输出
    write_srt(clean, output_file)
    print(f"\n输出: {output_file}")

if __name__ == "__main__":
    main()

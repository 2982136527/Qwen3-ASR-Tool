def is_gambling_ad(text):
    """Check if text is a gambling ad"""
    # Remove newlines for detection
    t = text.replace("\n", "").strip()
    if not t:
        return False
    for p in GAMBLING_PATTERNS:
        if p in t:
            return True
    return False


def is_gambling_ad_text(text):
    """More aggressive detection for pre-乾杯 garbage Chinese"""
    t = text.replace("\n", "").strip()
    if not t:
        return False
    
    # If the text has ANY Japanese (hiragana/katakana), it's NOT gambling ad
    if re.search(r'[\u3040-\u309f\u30a0-\u30ff]', t):
        return False
    
    # If mostly Chinese characters but contains gambling keywords
    has_gambling = any(p in t for p in GAMBLING_PATTERNS)
    if has_gambling:
        return True
    
    # Check if almost all chars are in the Chinese ad set
    chinese_ratio = sum(1 for c in t if '\u4e00' <= c <= '\u9fff' or c in '，。！？、')
    total_letters = sum(1 for c in t if c.isalpha())
    if total_letters > 0 and chinese_ratio / total_letters > 0.5:
        # Mostly Chinese - could be gambling ad
        # But only flag if short and meaningless
        if len(t) <= 10 and not re.search(r'[\u3040-\u309f\u30a0-\u30ff]', t):
            return True
    
    return False


def greedy_translate(text):
    """
    Translate Japanese text using longest matching strategy.
    Returns translated text. Falls back to original if nothing matches.
    """
    t = text.replace("\n", "").strip()
    if not t:
        return text
    
    # Clean the text for matching
    # Remove extra spaces
    t = re.sub(r'\s+', '', t)
    
    # Try exact match first
    if t in VOCAB_DICT:
        return VOCAB_DICT[t]
    
    # Try to split into known sub-phrases using longest matching
    result_parts = []
    i = 0
    matched_any = False
    
    while i < len(t):
        matched = False
        for jp, cn in VOCAB:
            if jp and t[i:].startswith(jp):
                result_parts.append(cn)
                i += len(jp)
                matched = True
                matched_any = True
                break
        if not matched:
            # Try matching single character
            char = t[i]
            i += 1
            
            # Check if it's known as a single character
            single_match = VOCAB_DICT.get(char)
            if single_match:
                result_parts.append(single_match)
                matched_any = True
            else:
                result_parts.append(char)
        # Safety limit
        if i > 500:
            break
    
    if matched_any:
        return "".join(result_parts)
    
    # If nothing matched and it's a short fragment, make a best guess
    return text


def parse_srt(filepath):
    """Parse SRT file into segments"""
    with open(filepath, 'r', encoding='utf-8') as f:
        content = f.read()
    
    # Split by blank lines to get segments
    segments = []
    raw_segments = re.split(r'\n\n+', content.strip())
    
    seg_num = 0
    for raw in raw_segments:
        lines = raw.strip().split('\n')
        if len(lines) < 2:
            continue
        seg_num += 1
        
        # Parse sequence number
        seq_match = re.match(r'^(\d+)$', lines[0].strip())
        if not seq_match:
            continue
        
        seq = int(seq_match.group(1))
        
        # Parse timecode
        tc_match = re.match(r'^(\d{2}:\d{2}:\d{2}[.,]\d{3})\s*-->\s*(\d{2}:\d{2}:\d{2}[.,]\d{3})$', lines[1].strip())
        if not tc_match:
            continue
        
        start_time = tc_match.group(1)
        end_time = tc_match.group(2)
        
        # Text content (remaining lines)
        text = '\n'.join(lines[2:])
        
        segments.append({
            'seq': seq,
            'start': start_time,
            'end': end_time,
            'text': text
        })
    
    return segments


def write_srt(segments, filepath):
    """Write segments to SRT file with sequential numbering"""
    with open(filepath, 'w', encoding='utf-8') as f:
        for i, seg in enumerate(segments, 1):
            f.write(f"{i}\n")
            f.write(f"{seg['start']} --> {seg['end']}\n")
            f.write(f"{seg['text']}\n")
            f.write("\n")


